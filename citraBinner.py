import matplotlib.pyplot as plt
import numpy as np


def show(img, cmap=None):
    cmap = cmap or plt.cm.gray
    fig, ax = plt.subplot(1, 1, figsize=(8, 6))
    ax.imshow(img, cmap=cmap)
    ax.set_axis_of()
    plt.show()


img = coins()
show(img)
